<p>
Ethan Beattie founded Beattie Law Firm, PLLC in Durham, North Carolina
in October 2008.  A former associate with the Law Office of Orlando
A. Gamarra in Arlington, Virginia, Ethan received his juris doctor
degree from William Mitchell College of Law in St. Paul, Minnesota in
2006.  While at William Mitchell, Ethan was a research assistant to
Professor A. John Radsan and participated in the Rosalie Wahl Moot
Court Competition.  Ethan graduated <i>cum laude</i> from Virginia
Commonwealth University in 2003 with a bachelor of arts in History.
Prior to attending Virginia Commonwealth, Ethan served four years as a
Radioman in the United States Navy onboard USS Thorn.  Ethan and his
wife, Puja Puri, live in Chapel Hill, North Carolina.
</p>

<p>
Ethan is a member of the American Immigration Lawyers Association
(AILA), the North Carolina Fourteenth Judicial District Bar, and the
Durham County Bar Association (DCBA).
